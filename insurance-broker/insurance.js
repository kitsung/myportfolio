// Bona Fide Check //
function bfscPaymentCheck(arg) {

    const salesSum = getSalesEl.value;
    const bfscSum = getBFSCPaymentsEl.value;
    const bfscAmount = getAnnualBFSCEl.value;
    let bfscExceed = false;
    
    if((bfscSum/salesSum) <= arg.bfscPercentage){
        bfscExceed = true;

        if(arg.rating != "roles"){
            if(bfscAmount <= arg.max || salesSum < arg.payment){
                finalList.push(`${arg.name} | Tel: ${arg.tel}`);
            };
        }else{
            finalList.push(`${arg.name} | Tel: ${arg.tel}`);
    };
    };

};

